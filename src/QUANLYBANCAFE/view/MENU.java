    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = MenuTable.getSelectedRow();
        
        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xoá!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int madouong = (int) MenuTable.getValueAt(selectedRowIndex, 0);
        
        int confirmResult = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xoá món này khỏi menu?", "Xác nhận xoá", JOptionPane.YES_NO_OPTION);
        if(confirmResult == JOptionPane.YES_OPTION){
            defaultTableModel.removeRow(selectedRowIndex);
            
            menudouongservice.deleteMenu(madouong);
            
            JOptionPane.showConfirmDialog(this, "Xoá món ra khỏi menu thành công!!!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

