    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            int MaDoUong_ID = Integer.parseInt(txtID.getText());
            String Name = txtName.getText();
            String Dongia = txtDongia.getText();
            int MaDanhMuc_ID = Integer.parseInt(txtMaDM.getText());

            menu = new MENUDOUONG();
            menu.setMaDoUong_ID(MaDoUong_ID);
            menu.setTenDoUong(Name);
            menu.setDonGia_ID(Dongia);
            menu.setMaDanhMuc_ID(MaDanhMuc_ID);

            menudouongservice.addMenu(menu);

            defaultTableModel.setRowCount(0);
            setTableData(menudouongservice.getAllMenuDoUong());
            JOptionPane.showMessageDialog(this, "Thêm Menu thành công!");
            clearFields();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Mã Đồ Uống và Mã Danh Mục phải là số nguyên!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

